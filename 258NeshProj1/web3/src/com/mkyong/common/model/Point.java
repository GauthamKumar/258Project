package com.mkyong.common.model;

public class Point 
{
	private double x;
    private double y;
    // + getters, setters
    
    public Point()
    {
    	
    }
    
    public double getX()
    {
    	return x;
    }

    public double getY()
    {
    	return y;
    }
    
    public void setX(double x)
    {
    	this.x = x;
    }


    public void setY(double y)
    {
    	this.y = y ;
    }    	

}
